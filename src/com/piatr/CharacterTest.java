package com.piatr;


public class CharacterTest {
    public static void main(String[] args) {
        System.out.println("new line\n\n\naaa");
        System.out.println("carriage return\r\r\raaa");
        System.out.println("Formfeed\f\f\faaa");
        System.out.println("Backspace\b\b\baaa");
        System.out.println("Tab\t\t\taaa");
        System.out.println("Double quote\"\"\"aaa");
        System.out.println("Single quote\'\'\'aaa");
        System.out.println("Backslash\\\\\\aaa");
    }
}
